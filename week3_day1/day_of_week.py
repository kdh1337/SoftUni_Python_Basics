number = int(input())

if number == 1:
    number = 'Monday'
elif number == 2:
    number = 'Tuesday'
elif number == 3:
    number = 'Wednesday'
elif number == 4:
    number = 'Thursday'
elif number == 5:
    number = 'Friday'
elif number == 6:
    number = 'Saturday'
elif number == 7:
    number = 'Sunday'
else:
    number = 'Error'

print(number)